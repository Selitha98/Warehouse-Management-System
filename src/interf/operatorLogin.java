    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        
        if (OpEmail.getText().isEmpty()||OpPass.getText().isEmpty())
        {
        
            JOptionPane.showMessageDialog(this, "Missing Informations.");
        
        }else{
            String Query = " select * from warehouse.operatoraccount where Email ='"+OpEmail.getText()+"' and Password = '"+OpPass.getText()+"'";
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/warehouse","root","");
            St = conn.createStatement();
            Rs = St.executeQuery(Query);
            
            if (Rs.next()) 
            {
                new OperatorMain().setVisible(true);
                this.dispose();
            } else 
            {
                JOptionPane.showMessageDialog(this, "Wrong User Email or Password");
            }
          } 
        
        catch (Exception e) 
        {
            e.printStackTrace();
        }
        }
           
       
    }//GEN-LAST:event_jButton4MouseClicked

